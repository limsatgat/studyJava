public class V3_StringEx {

    // 문자열 리터럴
    // 덧셈 연산자를 이용한 문자열 결합
    public static void main(String[] args) {

        String name ="Soo" + "Heon";
        String str = name + 29;

        System.out.println(name);
        System.out.println(str);

        System.out.println(7 + " ");
        System.out.println(" " + 7);
        System.out.println(7 + "");
        System.out.println("" + 7);
        System.out.println("" + "");
        System.out.println(7 + 7 + "");
        System.out.println("" + 7 + 7);
    }
}
